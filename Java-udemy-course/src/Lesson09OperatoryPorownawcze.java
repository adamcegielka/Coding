public class Lesson09OperatoryPorownawcze {

    public static void main(String[] args) {
        int firstNumber = 10;
        int secondNumber = 3;

        boolean result = firstNumber > secondNumber;   // true
        System.out.println(result);

        System.out.println(firstNumber == secondNumber);      // false
        System.out.println(firstNumber != secondNumber);      // true
        System.out.println(firstNumber >= secondNumber);      // true
        System.out.println(firstNumber <= secondNumber);      // false
        System.out.println(firstNumber > secondNumber);        // true
        System.out.println(firstNumber < secondNumber);        // false
    }
}

/*
OPERATORY PORÓWNANIA:

==   równy
!=   różny
>=   większy lub równy
<=   mniejszy lub równy
>    większy
<    mniejszy

 */